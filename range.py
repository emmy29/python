#range function#
for i in range(10):
    print(i)


for i in range(1,50,3):
    print(i)
    


for i in range(10):
    if i==6:
        break
    print(i,end=',')




for i in range(20):
    if i%2==0:
        print("even",i)
        continue
    print("odd",i)
       

    
   


