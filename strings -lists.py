# strings <-->lists
print('ham cheese bacon'.split())#spilt the string into list 
print('03-30-2018'.split(sep='-'))#seprate the given character from string and convert it to list
print(', '.join(['Eric','John','Michael']))




#string formatting
print('{} {}'.format('monty','python'))#format the list according to the given format
print('{}{}'.format('monty','python'))#no space between curly braces



#fancier formatting
print('%s,%s,%s,(act %d)'.format('monty','python','adarsh',5))


